    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
         if(subscriberList.isEmpty()){
            JOptionPane.showMessageDialog(null,"Sorry, there are no subscriber information to search");
        }
        else{
            String searchTerm = JOptionPane.showInputDialog(null, "Please enter an email address to search for");
            
            boolean found = false;
            for(Subscriber s:subscriberList){
                if(s.getEmail().equalsIgnoreCase(searchTerm)){
                    JOptionPane.showMessageDialog(null,s.getDetails());
                    found = true;
                    break;  // if the email is found
                }
            }
            if(!found){
            JOptionPane.showMessageDialog(null, "No matching email found.");
        }
   
        }
         
          
    }//GEN-LAST:event_SearchBtnActionPerformed

    private void DisplayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplayBtnActionPerformed
        // TODO add your handling code here:
         for(Subscriber s:subscriberList){
            JOptionPane.showMessageDialog(null,s.getDetails());
        }
         
         File f;
        FileInputStream fStream;
        ObjectInputStream oStream;
        
        try{
            f = new File("Subscribe.dat");
            fStream = new FileInputStream(f);
            oStream = new ObjectInputStream(fStream);
            
            subscriberList = (ArrayList<Subscriber>)oStream.readObject();
            oStream.close();
        }
        catch(IOException|ClassNotFoundException e){
            System.out.println(e);
        }
  
    }//GEN-LAST:event_DisplayBtnActionPerformed

