    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
        // TODO add your handling code here:
        
        
    // Check if Name and Email are not empty, and AgreeCb is selected
    if (NameTxt.getText().isEmpty() || EmailTxt.getText().isEmpty() || !AgreeCb.isSelected()) {
        // Show an error message 
        JOptionPane.showMessageDialog(null, "Please fill out all the required fields");
        return;
    }

    //add Subscriber data to an ArrayList and pass it to UnsubscribeGUI
        Subscriber s = new Subscriber();
            s.setEmail(EmailTxt.getText());
            s.setName(NameTxt.getText());
            subscriberList.add(s);  
            
        // Pass the subscriberList to UnsubscribeGUI
        UnsubscribeGUI newUnsubscribe = new UnsubscribeGUI(subscriberList);

        // make the new ui appear
        newUnsubscribe.setVisible(true);
        
        File f;
        FileOutputStream fStream;
        ObjectOutputStream oStream;
        
        try{
            f = new File("Subscribe.dat");
            fStream = new FileOutputStream(f);
            oStream = new ObjectOutputStream(fStream);
            
            oStream.writeObject(subscriberList);
            oStream.close();
        }
        catch(IOException e){
            System.out.println(e);
        }
        
    }//GEN-LAST:event_SubmitBtnActionPerformed

